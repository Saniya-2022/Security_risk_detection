@app.post("/control/start-events")
async def start_event_generation():
    """Manually start event generation (if stopped)"""
    global event_generation_task
    
    if not event_generator.running:
        event_generation_task = asyncio.create_task(
            event_generator.start(process_generated_event)
        )
        return {"message": "Event generation started", "status": "running"}
    else:
        return {"message": "Event generation already running", "status": "running"}


@app.post("/control/stop-events")
async def stop_event_generation():
    """Manually stop event generation (for testing)"""
    
    event_generator.stop()
    
    if event_generation_task:
        event_generation_task.cancel()
    
    return {"message": "Event generation stopped", "status": "stopped"}


