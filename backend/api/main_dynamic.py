event_generation_task = None


# ============================================
# EVENT PROCESSING CALLBACK
# ============================================

async def process_generated_event(event: Dict[str, Any]):
    """Callback to process events generated by event generator"""
    try:
        # Process event through alert service
        alert = await alert_service.process_event(event)
        
        # Broadcast updated statistics
        await broadcast_statistics()
        
    except Exception as e:
        logger.error(f"Error processing generated event: {e}")


async def broadcast_statistics():
    """Calculate and broadcast current statistics"""
    try:
        # Get statistics from MongoDB
        total = alerts_collection.count_documents({})
        high = alerts_collection.count_documents({"severity": "HIGH"})
        medium = alerts_collection.count_documents({"severity": "MEDIUM"})
        low = alerts_collection.count_documents({"severity": "LOW"})
        
        # Get threat distribution
        pipeline = [
            {"$group": {"_id": "$threat_type", "count": {"$sum": 1}}},
            {"$sort": {"count": -1}},
            {"$limit": 10}
        ]
        threat_distribution = list(alerts_collection.aggregate(pipeline))
        
        # Get blocked IPs count
        blocked_ips_count = len(get_blocked_ips())
        
        stats = {
            "total_alerts": total,
            "by_severity": {
                "HIGH": high,
                "MEDIUM": medium,
                "LOW": low
            },
            "threat_distribution": threat_distribution,
            "blocked_ips": blocked_ips_count,
            "active_connections": manager.get_connection_count(),
            "timestamp": datetime.utcnow().isoformat()
        }
        
        await manager.broadcast_statistics(stats)
        
    except Exception as e:
        logger.error(f"Error broadcasting statistics: {e}")


# ============================================
# STARTUP & SHUTDOWN EVENTS
# ============================================

@app.on_event("startup")
async def startup_event():
    """Start background tasks on application startup"""
    global event_generation_task
    
    logger.info("🚀 Starting Mini SIEM Dynamic System...")
    
    # Start WebSocket heartbeat
    manager.start_heartbeat()
    
    # Start event generator
    event_generation_task = asyncio.create_task(
        event_generator.start(process_generated_event)
    )
    
    logger.info("✅ Mini SIEM is now LIVE and generating events!")


@app.on_event("shutdown")
async def shutdown_event():
    """Cleanup on application shutdown"""
    logger.info("🛑 Shutting down Mini SIEM...")
    
    # Stop event generator
    event_generator.stop()
    
    if event_generation_task:
        event_generation_task.cancel()
    
    logger.info("✅ Mini SIEM shutdown complete")


