@app.get("/alerts")
async def get_all_alerts(limit: int = 100):
    """Get all alerts with optional limit"""
    
    alerts = alerts_collection.find().sort("timestamp", -1).limit(limit)
    
    result = []
    for alert in alerts:
        alert["_id"] = str(alert["_id"])
        result.append(alert)
    
    return result


@app.get("/alerts/severity/{severity}")
async def get_alerts_by_severity(severity: str):
    """Get alerts filtered by severity"""
    
    alerts = alerts_collection.find({"severity": severity.upper()}).sort("timestamp", -1)
    
    result = []
    for alert in alerts:
        alert["_id"] = str(alert["_id"])
        result.append(alert)
    
    return result


@app.get("/alerts/stats")
async def get_alert_statistics():
    """Get alert statistics"""
    
    total = alerts_collection.count_documents({})
    high = alerts_collection.count_documents({"severity": "HIGH"})
    medium = alerts_collection.count_documents({"severity": "MEDIUM"})
    low = alerts_collection.count_documents({"severity": "LOW"})
    
    # Get threat type distribution
    pipeline = [
        {"$group": {"_id": "$threat_type", "count": {"$sum": 1}}},
        {"$sort": {"count": -1}}
    ]
    threat_distribution = list(alerts_collection.aggregate(pipeline))
    
    # Get recent alerts timeline (last hour)
    from datetime import timedelta
    one_hour_ago = datetime.utcnow() - timedelta(hours=1)
    recent_alerts = alerts_collection.count_documents({
        "timestamp": {"$gte": one_hour_ago}
    })
    
    return {
        "total_alerts": total,
        "by_severity": {
            "HIGH": high,
            "MEDIUM": medium,
            "LOW": low
        },
        "threat_distribution": threat_distribution,
        "recent_alerts_last_hour": recent_alerts
    }


@app.get("/alerts/timeline")
async def get_alerts_timeline(minutes: int = 60):
    """Get alerts timeline for last N minutes"""
    
    from datetime import timedelta
    
    time_ago = datetime.utcnow() - timedelta(minutes=minutes)
    
    pipeline = [
        {"$match": {"timestamp": {"$gte": time_ago}}},
        {"$group": {
            "_id": {
                "$dateToString": {
                    "format": "%Y-%m-%d %H:%M",
                    "date": "$timestamp"
                }
            },
            "count": {"$sum": 1}
        }},
        {"$sort": {"_id": 1}}
    ]
    
    timeline = list(alerts_collection.aggregate(pipeline))
    
    return {
        "timeline": timeline,
        "period_minutes": minutes
    }


